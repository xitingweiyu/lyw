package com.wonders.bud.framework.common.bo;

/**
 * 移动端 -用户登录后存储的信息BO
 * @author Dy
 *
 */
public class UserBO {

	private int uid;
	private String username;
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
}
